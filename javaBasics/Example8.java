package com.javaBasics;

public class Example8 {

	public static void main(String[] args) {

		char ch;
		
		System.out.println("UpperCase Alphabets");
		
		for(ch = 'A'; ch <= 'Z'; ch++)
		{
			System.out.println(ch + "  " );
		}
			
		System.out.println(" ");
		
		System.out.println("LowerCase Alphabets");
		
		for(ch = 'a'; ch <= 'z'; ch++)
		{
			System.out.println(ch + "  " );
			
		}
		
	}

}
